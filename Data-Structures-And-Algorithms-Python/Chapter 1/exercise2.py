num_a = int(input("Please enter the first number: "))
num_b = int(input("Please enter the second number: "))

print(f"{num_a} + {num_b} is {num_a + num_b}")
print(f"{num_a} - {num_b} is {num_a - num_b}")
print(f"{num_a} * {num_b} is {num_a * num_b}")
print(f"{num_a} / {num_b} is {num_a / num_b}")
print(f"{num_a} % {num_b} is {num_a % num_b}")
print(f"{num_a} ^ {num_b} is {num_a ** num_b}")